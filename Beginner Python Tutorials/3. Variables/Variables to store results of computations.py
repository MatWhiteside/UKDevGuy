# Example of using variables for addition
# Total number of additions: 3

# Result: 9
first_addition = 5 + 4
print(first_addition)
# Result: 10
second_addition = first_addition + 1
print(second_addition)
# Result: 11
print(second_addition + 1)


# Example of not using variables for the same addition
# Total number of additions: 6

# Result: 9
print(5 + 4)
# Result: 10
print(5 + 4 + 1)
# Result: 11
print(5 + 4 + 1 + 1)